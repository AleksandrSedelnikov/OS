echo -n "Введите IP: "; read ip # вводится ip
echo -n "Введите Mask: "; read mask # вводится маска
IFS=. # разделятор слов
read -r i1 i2 i3 i4 <<< "$ip" # числа разделенные точкой записываются в переменные
read -r m1 m2 m3 m4 <<< "$mask" # числа разделенные точкой записываются в переменные
echo "Исходный IP-address: $((i1 & m1))"."$((i2 & m2))"."$((i3 & m3))"."$((i4 & m4))" # побитовая сумма